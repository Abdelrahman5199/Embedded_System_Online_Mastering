void NVIC_EnableIRQ(IRQn_Type IRQn)
{
	NVIC ->ISER[IRQn/32U] |= (1U<<(IRQn%32));
}
/*====================================================   END_ FUNCTION   ====================================================*/
/*====================================================   Start_FUNCTION   ====================================================*/
void NVIC_DisableIRQ(IRQn_Type IRQn)
{
	NVIC ->ICER[IRQn/32U] &=~ (1U<<(IRQn%32));
}
